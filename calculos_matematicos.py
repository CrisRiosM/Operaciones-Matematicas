def sumar(op1,op2):
    print("resultado de la suma: ", op1 + op2 )

def restar(op1,op2):
    print("resultado de la resta: ", op1 - op2 )

def multiplicar(op1,op2):
    print("resultado de la multiplicacion: ", op1 * op2 )

def dividir(op1,op2):
    print("resultado de la division: ", op1 / op2 )

def potencia(op1,op2):
    print("resultado de la potencia: ", op1 ** op2 )

def porcentaje(op1,op2):
    print("el", op2, "%", "de", op1, "es : ", op1*(op2/100) )


